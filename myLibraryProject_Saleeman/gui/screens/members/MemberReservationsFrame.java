    private void initViews() {
        DefaultListModel model = new DefaultListModel();
        int i = 0;
        for (Reservation reservation : listener.getReservations()) {
            if (selectedMember == null || reservation.getMember().equals(selectedMember)) {
                model.add(i++, reservation);
            }
        }
        reservationsList.setModel(model);
        if (selectedMember == null) {
            pickupReservation.setEnabled(false);
            cancelReservation.setEnabled(false);
        }
    }

