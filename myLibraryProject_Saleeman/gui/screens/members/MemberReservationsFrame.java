    private void pickupReservationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pickupReservationActionPerformed

        int index = reservationsList.getSelectedIndex();
        if (index == -1) {
            JOptionPane.showMessageDialog(null, "You must select a reservation first!");
            return;
        }
        Reservation selectedReservation = listener.getReservations().get(index);
        selectedMember.pickupReservation(selectedReservation);
        listener.onReservationPickedUp(selectedReservation);
        initViews();
    }//GEN-LAST:event_pickupReservationActionPerformed

    private void cancelReservationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelReservationActionPerformed
        int index = reservationsList.getSelectedIndex();
        if (index == -1) {
            JOptionPane.showMessageDialog(null, "You must select a reservation first!");
            return;
        }
        Reservation selectedReservation = listener.getReservations().get(index);
        selectedMember.cancelReservation(selectedReservation);
        listener.onReservationPickedUp(selectedReservation);
        initViews();
    }//GEN-LAST:event_cancelReservationActionPerformed


