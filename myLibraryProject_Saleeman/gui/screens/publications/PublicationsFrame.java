    private void addCopyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCopyButtonActionPerformed
        int selected = publicationsList.getSelectedIndex();
        if (selected > -1) {
            Publication publication = publicationsListener.getPublications().get(selected);
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure you want to add a new copy?", "Add New Copy", JOptionPane.YES_NO_OPTION);
            if (dialogResult == JOptionPane.YES_OPTION) {
                publication.addCopy();
                initView();
            }
        }
    }//GEN-LAST:event_addCopyButtonActionPerformed

