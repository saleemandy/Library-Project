    private void initView() {

        DefaultListModel<Publication> publicationsListModel = new DefaultListModel<>();
        int i = 0;
        for (Publication publication : publicationsListener.getPublications()) {
            publicationsListModel.add(i++, publication);
        }
        publicationsList.setModel(publicationsListModel);

    }
